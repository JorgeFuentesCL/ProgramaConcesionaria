    private void jlabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlabel2MouseClicked
        DefaultTableModel dtm = (DefaultTableModel) this.tablaVehiculos.getModel();
        //Rescatamos el indice de la fila seleccionada
        int fila = this.tablaVehiculos.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(null, "No se ha seleccionado un vehiculo");
        } else {
            //Rescatamos los datos de la fila seleccionada (marca, modelo, pantent,...)
            String marca = String.valueOf(this.tablaVehiculos.getValueAt(fila, 0));
            String modelo = String.valueOf(this.tablaVehiculos.getValueAt(fila, 1));
            String patente = String.valueOf(this.tablaVehiculos.getValueAt(fila, 2));
            String color = String.valueOf(this.tablaVehiculos.getValueAt(fila, 3));
            String anio = String.valueOf(this.tablaVehiculos.getValueAt(fila, 4));
            int kilometraje = Integer.parseInt(String.valueOf(this.tablaVehiculos.getValueAt(fila, 5)));
            int precio = Integer.parseInt(String.valueOf(this.tablaVehiculos.getValueAt(fila, 6)));
            //Creamos un nuevo objetivo vehiculo con los datos que rescatamos
            Vehiculo vc = new Vehiculo(marca,modelo,patente,color,anio,kilometraje,precio);
            //Llamamos al metodo modificar del menu principal y le pasamos por parametro el objeto vehiculo creado
            mp.modificarVehiculo(vc);
            JOptionPane.showMessageDialog(null, "Registro modificado!");
        }
        
    }//GEN-LAST:event_jlabel2MouseClicked


